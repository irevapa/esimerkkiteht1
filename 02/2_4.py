# ohjelma, joka kysyy kolme kokonaislukua
x = float(input("syötä ensimmäinen kokonaisluku: "))
y = float(input("syötä toinen kokonaisluku: "))
z = float(input("syötä kolmas kokonaisluku:" ))

summa = x + y + z
tulo = x * y * z
keskiarvo = summa / 3

print("summa: ", summa)
print("tulo: ", tulo)
print("keskiarvo: ", keskiarvo)